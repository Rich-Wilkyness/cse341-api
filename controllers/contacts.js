const mongodb = require('../db/connect');
const ObjectId = require('mongodb').ObjectId;

const getAllContacts = async (req, res, next) => {
  const result = await mongodb.getDb().db().collection('contacts').find();
  result.toArray().then((lists) => {
    res.setHeader('Content-Type', 'application/json');
    res.status(200).json(lists);
  });
};

const getContact = async (req, res, next) => {
  // const contactId = req.query.contactId; // Get the contactId from the query parameters
  // if (!contactId) {
  //   return res.status(400).json({ error: 'ContactId parameter is required' });
  // }
  const contactId = new ObjectId(req.params.id)

  const result = await mongodb.getDb().db().collection('contacts').find({ _id: contactId });
  result.toArray().then((lists) => {
    res.setHeader('Content-Type', 'application/json');
    res.status(200).json(lists);
  });
};

module.exports = { 
  getAllContacts,
  getContact,
 };
















































































































































































































































































































































































































































